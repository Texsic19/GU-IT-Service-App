def icon(name: str, size: int = 16, color: str = "currentColor", cls: str = "") -> str:
    """Return an inline Lucide SVG string."""
    paths = ICONS.get(name, ICONS["circle-dot"])
    return (
        f'<svg xmlns="http://www.w3.org/2000/svg" width="{size}" height="{size}" '
        f'viewBox="0 0 24 24" fill="none" stroke="{color}" stroke-width="2" '
        f'stroke-linecap="round" stroke-linejoin="round" '
        f'style="display:inline-block;vertical-align:middle" class="{cls}">'
        f'{paths}</svg>'
    )

def icon_text(icon_name: str, text: str, size: int = 16, color: str = "currentColor", gap: int = 6) -> str:
    """Return an icon + text span, inline."""
    return (
        f'<span style="display:inline-flex;align-items:center;gap:{gap}px">'
        f'{icon(icon_name, size, color)}'
        f'<span>{text}</span>'
        f'</span>'
    )

def icon_header(icon_name: str, text: str, level: int = 2, color: str = "currentColor") -> str:
    """Return a heading with an icon prefix."""
    sizes = {1: 28, 2: 24, 3: 20, 4: 18}
    sz = sizes.get(level, 20)
    tag = f"h{level}"
    return (
        f'<{tag} style="display:flex;align-items:center;gap:10px;margin:0 0 0.5rem 0">'
        f'{icon(icon_name, sz, color)}'
        f'<span>{text}</span>'
        f'</{tag}>'
    )
